<?php

namespace app\types;

/**
 * Позиции (посадочные места/юниты) устройства в корзине/шкафу:
 * одиночные номера, диапазоны через тире и их перечисление через запятую.
 * Строковый тип — отличается от StringType только форматом заполнения.
 */
class RackUnitsType extends StringType
{
	public static function name(): string
	{
		return 'rack-units';
	}

	public function inputHint(): ?string
	{
		return 'Одна позиция или диапазон через тире; несколько — через запятую.<br>'
			.'<b>Пример 1:</b> 2 — занимает юнит №2<br>'
			.'<b>Пример 2:</b> 1-2 — занимает юниты с 1го по 2й<br>'
			.'<b>Пример 3:</b> 1-2,5-7 — занимает юниты с 1го по 2й и с 5го по 7й';
	}
}
